#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Генератор красивого Telegraph-справочника по SendFlow.
Запусти локально (нужен интернет):  python3 make_telegraph.py
Скрипт создаст страницу и выведет ссылку — вставь её в config.HELP_URL / кнопку.
Если пакета telegraph нет:  pip install telegraph
"""
import json
import os
import sys

HTML = """
<p><b>SendFlow</b> — бот для массовых рассылок по твоим группам и каналам с твоих Telegram-аккаунтов. Ниже — все возможности.</p>
<h3>Аккаунты</h3>
<p>Подключай несколько Telegram-аккаунтов (на подписке — без лимита). Для каждого можно задать свой прокси (SOCKS5/HTTP) — это снижает риск блокировок. Прямо из бота можно проверить аккаунт через @SpamBot.</p>
<h3>Рассылка</h3>
<p>Рассылай по всем чатам или строго по выбранным папкам (можно несколько). Режимы: только текст; медиа + текст (фото/видео с подписью); пересыл поста по ссылке; текст с кнопками-ссылками. Можно задать до 3 разных текстов — на каждую отправку берётся случайный, это защищает от спам-фильтра. Есть AI-улучшение текста под продажу, настройка числа повторов и задержки.</p>
<h3>Форматирование 1-в-1</h3>
<p>Текст уходит точь-в-точь: <b>жирный</b>, <i>курсив</i>, <u>подчёркнутый</u>, <s>зачёркнутый</s>, спойлер, цитата, <code>моноширинный</code>, ссылки и премиум-эмодзи. Фото и видео отправляются без изменений.</p>
<h3>Умные функции</h3>
<p>Авто-вступление: если чат требует подписку на каналы, бот сам вступит и отправит сообщение повторно. Авто-папка: все каналы, куда бот вступил во время рассылки, складываются в отдельную папку. Шаблоны: сохраняй готовые рассылки и запускай в один тап.</p>
<h3>Мониторинг</h3>
<p>В реальном времени видно: куда сейчас шлётся, что следующее, сколько отправлено и ошибок, прогресс по циклам. Рассылку можно ставить на паузу, продолжать и останавливать.</p>
<h3>Профиль и рост</h3>
<p>В профиле — твой тариф, срок подписки, аккаунты, рефералы и шаблоны. За каждого приглашённого друга — бонус к подписке.</p>
<h3>Подписка</h3>
<p>Оплата: Telegram Stars, карта, TON, крипта. Вопросы и ручная активация — пиши @zucag.</p>
<p>Поддержка: <a href="https://t.me/zucag">@zucag</a></p>
"""


def _create_page():
    """Создаёт страницу в Telegraph и возвращает URL (или бросает исключение)."""
    from telegraph import Telegraph
    tg = Telegraph()
    tg.create_account(short_name="SendFlow")
    res = tg.create_page(title="SendFlow — справочник", html_content=HTML, author_name="SendFlow")
    return "https://telegra.ph/" + res["path"]


def ensure_help_page(cache_path=None):
    """Возвращает URL справочника. Есть кэш — берём оттуда,
    иначе создаём страницу и кэшируем. Без интернета/пакета — None."""
    if cache_path and os.path.exists(cache_path):
        try:
            with open(cache_path, "r", encoding="utf-8") as f:
                u = json.load(f).get("url")
                if u:
                    return u
        except Exception:
            pass
    try:
        url = _create_page()
    except Exception:
        return None
    if cache_path:
        try:
            with open(cache_path, "w", encoding="utf-8") as f:
                json.dump({"url": url}, f)
        except Exception:
            pass
    return url


def main():
    url = ensure_help_page()
    if url:
        print("Готово! Ссылка на справочник:")
        print(url)
    else:
        print("Не удалось создать страницу. Установи пакет:  pip install telegraph")


if __name__ == "__main__":
    main()
