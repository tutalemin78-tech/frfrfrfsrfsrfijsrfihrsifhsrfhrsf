def _create_page():
    """Создаёт страницу в Telegraph и возвращает URL (или бросает исключение)."""
    from telegraph import Telegraph
    tg = Telegraph()
    tg.create_account(short_name="SendFlow")
    res = tg.create_page(title="SendFlow — справочник", html_content=HTML, author_name="SendFlow")
    return "https://telegra.ph/" + res["path"]


def ensure_help_page(cache_path=None):
    """Возвращает URL справочника. Есть кэш — берём оттуда,
    иначе создаём страницу и кэшируем. Без интернета/пакета — None."""
    if cache_path and os.path.exists(cache_path):
        try:
            with open(cache_path, "r", encoding="utf-8") as f:
                u = json.load(f).get("url")
                if u:
                    return u
        except Exception:
            pass
    try:
        url = _create_page()
    except Exception:
        return None
    if cache_path:
        try:
            with open(cache_path, "w", encoding="utf-8") as f:
                json.dump({"url": url}, f)
        except Exception:
            pass
    return url


def main():
    url = ensure_help_page()
    if url:
        print("Готово! Ссылка на справочник:")
        print(url)
    else:
        print("Не удалось создать страницу. Установи пакет:  pip install telegraph")


if __name__ == "__main__":
    main()
