def em(key):
    """Премиум-эмодзи для ТЕКСТА сообщений (нужен parse_mode=HTML)."""
    eid_, fb = EMO.get(key, ("", "•"))
    if PREMIUM_EMOJI and eid_:
        return '<tg-emoji emoji-id="%s">%s</tg-emoji>' % (eid_, fb)
    return fb


def eid(key):
    """ID премиум-эмодзи для icon_custom_emoji_id на КНОПКАХ."""
    eid_, _ = EMO.get(key, ("", ""))
    return eid_ or None


def fb(key):
    """Только запасной обычный эмодзи."""
    _, f = EMO.get(key, ("", "•"))
    return f
